def _clean_translated(text: str, max_len: int = 220) -> str:
    """تنظيف مخرجات الترجمة وقطعها عند جملة مكتملة"""
    if not text:
        return ""
    # إزالة عبارات الترجمة الشكلية
    boilerplate = [
        "وفقا للتقارير،", "وفقًا للتقارير،",
        "تجدر الإشارة إلى أن", "في سياق متصل،",
        "من الجدير بالذكر أن", "وفي هذا الإطار،",
        "للإشارة،", "جدير بالذكر أن", "يُشار إلى أنه",
        "وبحسب التقارير،", "وكشفت التقارير أن",
    ]
    for bp in boilerplate:
        text = text.replace(bp, "")
    text = re.sub(r'\s+', ' ', text).strip()

    if len(text) <= max_len:
        return text

    # قطع عند نهاية جملة مكتملة
    for punct in ['؟', '!', '.']:
        pos = text.rfind(punct, 0, max_len)
        if pos > max_len * 0.45:
            return text[:pos + 1].strip()

    pos = text.rfind(' ', 0, max_len)
    if pos > max_len * 0.5:
        return text[:pos].strip()

    return text[:max_len].strip()


def _extract_points(summary_ar: str, max_points: int = 3) -> list:
    """
    استخراج نقاط مكتملة المعنى من الملخص.
    قطع على نهايات الجمل فقط (. ! ؟) — لا على الفاصلة.
    """
    if not summary_ar:
        return []

    # قسّم على نهايات الجمل الحقيقية فقط
    sentences = re.split(r'(?<=[.؟!])\s+', summary_ar)
    sentences = [s.strip() for s in sentences if len(s.strip()) >= 18]

    points = []
    for sent in sentences[:6]:
        # تجاهل الجمل الناقصة (تنتهي بفاصلة أو حرف وصل)
        if re.search(r'[،,]$', sent):
            pos = sent.rfind('،')
            if pos > 18:
                sent = sent[:pos].strip()
            else:
                continue

        # تجاهل الجمل المفتتحة بحرف وصل وهي قصيرة
        if re.match(r'^[وفلأإ]\s', sent) and len(sent) < 35:
            continue

        # تقليم إلى 95 حرف
        trimmed = _trim_sentence(sent, 95)
        if trimmed and len(trimmed) >= 15:
            points.append(trimmed)

        if len(points) >= max_points:
            break

    return points


def _trim_sentence(text: str, limit: int) -> str:
    """تقليم الجملة مع الحفاظ على اكتمالها"""
    if len(text) <= limit:
        return text
    for punct in ['،', '.', '؛']:
        pos = text.rfind(punct, 0, limit)
        if pos > limit * 0.55:
            return text[:pos].strip()
    pos = text.rfind(' ', 0, limit)
    if pos > limit * 0.6:
        return text[:pos].strip()
    return text[:limit].strip()


def _pick_hook(category: str, entity: str = "") -> str:
    """اختيار هوك مناسب لفئة الخبر"""
    pool = HOOK_MAP.get(category, HOOKS_GENERAL)
    hook = random.choice(pool)
    # إذا كان هناك اسم أداة/شركة مميز — أدرجه في الهوك
    if entity and random.random() < 0.4:
        hook = hook.rstrip(":") + f" ({entity}):"
    return hook


class ContentAgent:
    """
    وكيل الصياغة والتدقيق.
    يأخذ مقالة مُحلَّلة من SearchAgent ويُنتج تغريدة احترافية.
    """

    def __init__(self):
        self.name = "ContentAgent"

    def build_tweet(self, article: dict) -> str:
        """
        بناء تغريدة إبداعية كاملة من مقالة.
        النمط: هوك → وصف → نقاط → ختام سعودي
        """
        title_en   = article.get("title",   "") or ""
        summary_en = (article.get("summary", "") or "")[:500]
        entities   = article.get("entities", {})
        category   = entities.get("category", "general")
        tool       = entities.get("tool",     "")
        company    = entities.get("company",  "")
        entity_name = tool or company

        # ── ترجمة وتنظيف ────────────────────────────────────
        title_ar   = _clean_translated(
            saudify(translate_to_arabic(title_en, max_len=170)),
            max_len=170
        )
        summary_ar = _clean_translated(
            saudify(translate_to_arabic(summary_en, max_len=450)),
            max_len=420
        ) if summary_en else ""

        # ── اختيار العناصر ──────────────────────────────────
        hook      = _pick_hook(category, entity_name)
        connector = random.choice(CONNECTORS)
        closing   = random.choice(CLOSINGS)
        add_local = random.random() < 0.30
        local_note = random.choice(LOCAL_NOTES) if add_local else ""

        # ── استخراج نقاط ────────────────────────────────────
        points = _extract_points(summary_ar, max_points=3)

        # ══════════════════════════════════════════════════════
        # بناء تدريجي — من الأكثر تفصيلاً إلى الأبسط
        # ══════════════════════════════════════════════════════

        # النمط A — هوك + وصلة + عنوان + 3 نقاط + محلي + ختام
        if add_local and len(points) >= 3:
            tweet = (
                f"{hook}\n\n"
                f"{title_ar}\n\n"
                f"{connector}\n"
                f"← {points[0]}\n"
                f"← {points[1]}\n"
                f"← {points[2]}\n\n"
                f"{local_note}\n\n"
                f"{closing}"
            )
            if tweet_length(tweet) <= 270:
                return tweet

        # النمط B — هوك + عنوان + 2 نقاط + ختام
        if len(points) >= 2:
            tweet = (
                f"{hook}\n\n"
                f"{title_ar}\n\n"
                f"← {points[0]}\n"
                f"← {points[1]}\n\n"
                f"{closing}"
            )
            if tweet_length(tweet) <= 270:
                return tweet

        # النمط C — هوك + عنوان + نقطة + ختام
        if points:
            tweet = (
                f"{hook}\n\n"
                f"{title_ar}\n\n"
                f"← {points[0]}\n\n"
                f"{closing}"
            )
            if tweet_length(tweet) <= 270:
                return tweet

        # النمط D — هوك + عنوان + ختام
        tweet = f"{hook}\n\n{title_ar}\n\n{closing}"
        if tweet_length(tweet) <= 270:
            return tweet

        # النمط E — fallback آمن مع عنوان مختصر
        short_title = _trim_sentence(title_ar, 150)
        short_close = random.choice(["وش رأيكم؟ 💬", "كيف تشوفونه؟"])
        return f"{hook}\n\n{short_title}\n\n{short_close}"

    def audit_tweet(self, tweet_text: str) -> dict:
        """
        يُدقق في التغريدة ويُعيد تقرير الجودة.
        يتحقق من: الطول، اللهجة، الاكتمال، وجود هوك وختام.
        """
        issues = []
        length = tweet_length(tweet_text)

        if length < 180:
            issues.append(f"⚠️ قصيرة جداً ({length} حرف) — تحتاج محتوى أكثر")
        elif length > 270:
            issues.append(f"❌ طويلة جداً ({length} حرف) — تجاوزت الحد")

        # تحقق من وجود هوك
        first_line = tweet_text.split('\n')[0]
        if not any(e in first_line for e in ['🚨','⚡','🔥','🚀','💥','🧨','⚠️','🔴','💡','📌','💻','⚙️','🌐','🎯','🧠']):
            issues.append("⚠️ لا يوجد هوك واضح في السطر الأول")

        # تحقق من وجود ختام تفاعلي
        if not any(c in tweet_text for c in ['؟', '💬', 'شاركونا', 'رأيكم']):
            issues.append("⚠️ لا يوجد ختام تفاعلي (سؤال أو دعوة للتعليق)")

        # تحقق من الجمل الناقصة
        if re.search(r'[،]\s*$', tweet_text.strip()):
            issues.append("❌ التغريدة تنتهي بجملة ناقصة")

        # تحقق من اللهجة الرسمية الزائدة
        formal_signs = ["وفقًا", "يُشار إلى", "تجدر الإشارة", "يتعين", "ثمة", "إذ "]
        for sign in formal_signs:
            if sign in tweet_text:
                issues.append(f"⚠️ كلمة رسمية موجودة: '{sign}'")
                break

        score = max(0, 10 - len(issues) * 2)
        return {
            "length":  length,
            "score":   score,
            "issues":  issues,
            "passed":  len(issues) == 0,
        }

    def build_batch(self, articles: list) -> list:
        """
        يُنتج دفعة من التغريدات من قائمة مقالات.
        يتضمن تقرير جودة لكل تغريدة.
        """
        results = []
        for i, art in enumerate(articles, 1):
            tweet = self.build_tweet(art)
            audit = self.audit_tweet(tweet)
            logger.info(
                f"[{self.name}] #{i} | "
                f"{audit['length']} حرف | "
                f"جودة: {audit['score']}/10 | "
                f"{'✅' if audit['passed'] else '⚠️'} | "
                f"{art.get('source','?')}"
            )
            results.append({
                "tweet":    tweet,
                "article":  art,
                "audit":    audit,
                "image_url": art.get("image_url"),
            })
        return results
